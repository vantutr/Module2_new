public class Student {
    int id;
    String name;
    String email;
    int age;
    float grade;
    public Student(int id, String name, String email, int age, float grade) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.age = age;
        this.grade = grade;
    }
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public int getAge() {
        return age;
    }

    public float getGrade() {
        return grade;
    }
    public float setId() {
        return age;
    }

}
